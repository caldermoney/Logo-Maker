function renderTriangle(color) {
    return `<polygon points="50,15 100,100 0,100" fill="${color}" />`;
}

function renderCircle(color) {
    return `<circle cx="50" cy="50" r="50" fill="${color}" />`;
}

function renderSquare(color) {
    return `<rect width="100" height="100" fill="${color}" />`;
}
